# Basic calculator program


class CalculatorBackend():
    @staticmethod
    def add(x, y):
        print(x + y)

    @staticmethod
    def subtract(x, y):
        print(x - y)

    @staticmethod
    def multiply(x, y):
        print(x * y)

    @staticmethod
    def divide(x, y):
        print(x / y)




